import { ReactNode, useState } from 'react';
import './App.css';
import { Wrapper } from './hoc/Wrapper';

interface Props {
      children?: ReactNode;
      type?: string
      url?: string
      title?: string 
      views?: number
}

function New(props: Props) {
    return (
        <div className="wrap-item wrap-item-new">
            <span className="label">New!</span>
            {props.children}
        </div>
    )
};

function Popular(props: Props) {
  {console.log(props)}
    return (
        <div className="wrap-item wrap-item-popular">
            <span className="label">Popular!</span>
            {props.children}
        </div>
    )
};

function Article(props: Props) {
  
    return (
        <div className="item item-article">
            <h3><a href="#">{props.title}</a></h3>
            <p className="views">Прочтений: {props.views}</p>
        </div>
    )
};

function Video(props: Props) {
    return (
        <div className="item item-video">
            <iframe src={props.url} frameBorder="0" allow="autoplay; encrypted-media" allowFullScreen></iframe>
            <p className="views">Просмотров: {props.views}</p>
        </div>
    )
};

function List(props: { list: Props[]; }) {
    return props.list.map(item => {
        switch (item.type) {
            case 'video':
              const VideoBlockWithWrapper = 
              item.views > 1000 ? Wrapper(Video, Popular)
              : item.views < 100 ? Wrapper(Video, New)
              : Video;
                return (
                    <VideoBlockWithWrapper {...item} />
                );

            case 'article':
              const ArticleBlockWithWrapper = 
                item.views > 1000 ? Wrapper(Article, Popular)
                : item.views < 100 ? Wrapper(Article, New)
                : Article;
                return (
                    <ArticleBlockWithWrapper {...item} />
                );
        }
    });
};

